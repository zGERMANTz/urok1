using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OperatorsOfSravneniaAndOtnosenii
{
    internal class Program
    {
        static void Main(string[] args)
        {
            /*
             * операторы отношений
             * операторы сравнения 
             * == равно
             * != не равно 
             * > меньше
             * < больше
             * >= больше или равно 
             * <= меньше или равно 
             */
            int a = 5;
            int b = 5;
            Console.WriteLine(a == b);
            Console.WriteLine(b > a);
            Console.WriteLine(a < b);
            Console.WriteLine(b != a);
            Console.WriteLine(a <= b);
            Console.WriteLine(b >= a);
            //результат срвнение это булевый результат тру фолс


        }
